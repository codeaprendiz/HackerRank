        /*function to get the intersection point of two linked 
    lists head1 and head2 */
    static int getNode(SinglyLinkedListNode head1, SinglyLinkedListNode head2) { 
        int c1 = getCount(head1); 
        int c2 = getCount(head2); 
        int d; 
  
        if (c1 > c2) { 
            d = c1 - c2; 
            return _getIntesectionNode(d, head1, head2); 
        } else { 
            d = c2 - c1; 
            return _getIntesectionNode(d, head2, head1); 
        } 
    } 
  
    /* function to get the intersection point of two linked 
     lists head1 and head2 where head1 has d more nodes than 
     head2 */
    static int _getIntesectionNode(int d, SinglyLinkedListNode node1, SinglyLinkedListNode node2) { 
        int i; 
        SinglyLinkedListNode current1 = node1; 
        SinglyLinkedListNode current2 = node2; 
        for (i = 0; i < d; i++) { 
            if (current1 == null) { 
                return -1; 
            } 
            current1 = current1.next; 
        } 
        while (current1 != null && current2 != null) { 
            if(current1.hashCode()==current2.hashCode()) {          
                    return current1.data; 
            } 
            current1 = current1.next; 
            current2 = current2.next; 
        } 
  
        return -1; 
    } 
  
    /*Takes head pointer of the linked list and 
    returns the count of nodes in the list */
    static int getCount(SinglyLinkedListNode node) { 
        SinglyLinkedListNode current = node; 
        int count = 0; 
  
        while (current != null) { 
            count++; 
            current = current.next; 
        } 
  
        return count; 
    }
    
